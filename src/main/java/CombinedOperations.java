public class CombinedOperations {
    public static void main (String[] args){
        int a = 2;
        int b = 4;
        b +=a;
        System.out.println(b);
        b = 4;
        b -=a;
        System.out.println(b);
        b = 4;
        b *=a;
        System.out.println(b);
        b = 4;
        b /=a;
        System.out.println(b);
        b = 4;
        b %=a;
        System.out.println(b);

    }

}
